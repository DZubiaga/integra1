    private void cbIdParteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbIdParteActionPerformed
        int x;
         for(x = 0;x < Proyecto.getListaPartes().size() ;x++){
             if(Proyecto.getListaPartes().get(x).getIdParte() == Integer.parseInt(cbIdParte.getSelectedItem().toString()))
             {
                         
                tfKmInicial.setText(String.valueOf(Proyecto.getListaPartes().get(x).getKmInicial()));
                tfKmFinal.setText(String.valueOf(Proyecto.getListaPartes().get(x).getKmFinal()));
                tfGasolina.setText(String.valueOf(Proyecto.getListaPartes().get(x).getGasolina()));
                tfPeaje.setText(String.valueOf(Proyecto.getListaPartes().get(x).getPeaje()));
                tfDietas.setText(String.valueOf(Proyecto.getListaPartes().get(x).getDietas()));
                tfOtros.setText(String.valueOf(Proyecto.getListaPartes().get(x).getOtros()));
                taIncidencia.setText(String.valueOf(Proyecto.getListaPartes().get(x).getIncidencia()));
             }
      
        }
         
        habilitarCampos(); 
         
    }//GEN-LAST:event_cbIdParteActionPerformed

    private void aceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarActionPerformed

        
        try {
            
            Proyecto.actualizarViajes(Integer.parseInt(cbIdParte.getSelectedItem().toString()),fechaActual(),Integer.parseInt(tfKmInicial.getText()),
                   Integer.parseInt(tfKmFinal.getText()),Integer.parseInt(tfGasolina.getText()),Integer.parseInt(tfPeaje.getText()),
                   Integer.parseInt(tfDietas.getText()), Integer.parseInt(tfOtros.getText()),taIncidencia.getText(),"Abierto");//Hay que cambiarlo por Cerrado
            
           Proyecto.seleccionarPartesViaje(Integer.parseInt(cbIdParte.getSelectedItem().toString()));
           Proyecto.abrirVistaModificarParte();     
                
               
        } catch (Exception e) 
        {
            
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
    }//GEN-LAST:event_aceptarActionPerformed

