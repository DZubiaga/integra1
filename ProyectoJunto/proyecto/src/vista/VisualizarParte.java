    private void fechaOnCommit(datechooser.events.CommitEvent evt) {//GEN-FIRST:event_fechaOnCommit
        try {
            Proyecto.encontrarAbiertos((java.sql.Date) cambiarFecha());
            cbIdParte.setEnabled(true);
            rellenarComboBox();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
 
    }//GEN-LAST:event_fechaOnCommit

    public void rellenarComboBox()
    {
        int x;
        for(x=0;x<Proyecto.getListaPartes().size();x++)
        {
            cbIdParte.insertItemAt(Proyecto.getListaPartes().get(x).getIdParte(),x );
            
        }
    }
